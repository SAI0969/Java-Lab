class Relation{
  public static void main(String args[]){
    int a = 5, b = 6, c = 4, d = 8;
    System.out.println(a > b);
    System.out.println(a < b);
    System.out.println(c > d);
    System.out.println(c < d);
    System.out.println(a == b);
    System.out.println(c == d);
  }
}

// output
// false
// true
// false
// true
// false
// false
